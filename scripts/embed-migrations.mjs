#!/usr/bin/env node
/**
 * Embeds the schema migrations into a generated TS module so per-shop
 * Durable Object databases can bootstrap themselves with the exact same
 * schema the primary D1 runs — without any filesystem access at runtime.
 *
 * 0002_seed_demo_data.sql is deliberately excluded: it is Rezene's demo
 * content. New shops get a minimal neutral seed from the provisioner.
 *
 * Run via `npm run build` (prebuild) or `node scripts/embed-migrations.mjs`
 * after adding a migration; the output is committed.
 */
import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { createHash } from "node:crypto";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const dir = join(root, "migrations");
// 0002 = Rezene demo content; 0009 = the platform shop registry, which
// lives only in the primary D1 — shop databases must not carry a copy.
const EXCLUDE = new Set([
  "0002_seed_demo_data.sql",
  "0009_verto_shops.sql",
  "0010_platform_crm.sql", // Verto HQ CRM — platform-only, like the registry
  "0011_crm_health_inbox.sql", // CRM phase 2 — platform-only
  "0016_feedback.sql", // Support tickets land in the platform DB, not per-shop
  "0020_kb_overrides.sql", // KB overlay is platform-wide, not per-shop
  "0021_superadmin_role.sql", // Platform-owner role — only the platform DB has HQ
  "0045_school_certificates.sql", // Public credentials resolve at the platform, not per-shop
  "0047_library_cache.sql", // Library cache is shared across shops at the platform
  "0048_directory.sql", // Directory + maker waitlist live at the platform
  "0049_activation_events.sql", // Activation funnel — recorded at the platform, read by HQ
  "0055_ai_usage.sql", // AI usage ledger — platform-wide, read by HQ
  "0056_shop_metrics.sql", // Fleet revenue rollup — platform-wide, read by HQ
  "0057_platform_errors.sql", // Error/incident log — platform-wide, read by HQ
  "0060_hq_marketing.sql", // HQ marketing suite — Verto's own audience/sends, platform-only
  "0061_hq_sequences.sql", // HQ marketing lifecycle-sequence switches, platform-only
  "0062_referrals.sql", // Referral program — shops registry + credits, platform-only
]);

const files = readdirSync(dir)
  .filter((f) => f.endsWith(".sql") && !EXCLUDE.has(f))
  .sort();

const entries = files.map((name) => {
  const sql = readFileSync(join(dir, name), "utf8");
  return `  { name: ${JSON.stringify(name)}, sql: ${JSON.stringify(sql)} },`;
});

const out = `// GENERATED by scripts/embed-migrations.mjs — do not edit by hand.
// Schema migrations for per-shop Durable Object databases (demo seed excluded).

export interface EmbeddedMigration {
  name: string;
  sql: string;
}

export const SCHEMA_MIGRATIONS: EmbeddedMigration[] = [
${entries.join("\n")}
];
`;

writeFileSync(join(root, "src/worker/generated/migrations.ts"), out);

// The demo-shop seed: 0002's full fake catalog (Maison Atlantique) embedded
// separately so the platform can provision a rich public demo shop.
const demoSeed = readFileSync(join(dir, "0002_seed_demo_data.sql"), "utf8");

// The "active studio" layer on top of the catalog: live-looking orders,
// commissions, production tasks, supplier threads (scripts/demo-seed.mjs →
// scripts/demo-seed.sql). Fully idempotent (fixed *_demo_* ids, INSERT OR
// REPLACE), so the demo bootstrap can re-apply it whenever the version hash
// changes — that's how the live demo DO picks up seed updates on deploy.
const richSeed = readFileSync(join(root, "scripts/demo-seed.sql"), "utf8");
const version = createHash("sha256").update(demoSeed).update(richSeed).digest("hex").slice(0, 16);

const demoOut = `// GENERATED by scripts/embed-migrations.mjs — do not edit by hand.
// Full demo-content seed (Maison Atlantique) for the public demo shop.

export const DEMO_SEED_SQL: string = ${JSON.stringify(demoSeed)};

// Idempotent "active studio" top-up (orders, commissions, tasks, threads).
export const DEMO_RICH_SEED_SQL: string = ${JSON.stringify(richSeed)};

// Bumps whenever either seed changes; the demo shop re-applies on mismatch.
export const DEMO_SEED_VERSION = ${JSON.stringify(version)};
`;
writeFileSync(join(root, "src/worker/generated/demo-seed.ts"), demoOut);
console.log(`embedded ${files.length} migrations (excluded: ${[...EXCLUDE].join(", ")}) + demo seed v${version}`);
